def load_palette_from_image(path: Path) -> list[tuple[int, int, int]]:
    import pygame

    if not path.exists():
        return DAWNBRINGER32_FALLBACK.copy()

    image = pygame.image.load(str(path)).convert()
    colors: list[tuple[int, int, int]] = []
    seen: set[tuple[int, int, int]] = set()

    for y in range(image.get_height()):
        for x in range(image.get_width()):
            color = image.get_at((x, y))[:3]
            if color not in seen:
                seen.add(color)
                colors.append(color)
            if len(colors) == 32:
                return colors

    return colors or DAWNBRINGER32_FALLBACK.copy()
